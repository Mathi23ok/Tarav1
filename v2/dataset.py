from pathlib import Path
from typing import Dict, Optional, Tuple

import numpy as np
import torch
from torch.utils.data import Dataset, WeightedRandomSampler


class FeatureDataset(Dataset):
    """Dataset wrapper for precomputed feature vectors."""

    def __init__(self, features: np.ndarray, labels: np.ndarray, scores: np.ndarray):
        self.features = features.astype(np.float32)
        self.labels = labels.astype(np.int64)
        self.scores = scores.astype(np.float32)

    def __len__(self) -> int:
        return len(self.labels)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        return {
            "features": torch.from_numpy(self.features[idx]),
            "label": torch.tensor(self.labels[idx], dtype=torch.float32),
            "score": torch.tensor(self.scores[idx], dtype=torch.float32),
        }


def load_npz_data(path: str) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Load feature, label, and score arrays from an NPZ archive."""
    archive = np.load(path)
    features = archive["X"]
    labels = archive["y"]
    scores = archive["score"]
    return features, labels, scores


def normalize_features(
    features: np.ndarray,
    mean: Optional[np.ndarray] = None,
    std: Optional[np.ndarray] = None,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """Normalize features to zero mean and unit variance."""
    if mean is None or std is None:
        mean = np.mean(features, axis=0, keepdims=True)
        std = np.std(features, axis=0, keepdims=True)
    std = np.where(std < 1e-6, 1.0, std)
    normalized = (features - mean) / std
    return normalized, mean, std


def create_balanced_sampler(labels: np.ndarray) -> WeightedRandomSampler:
    """Create a sampler that balances classes during training."""
    class_counts = np.bincount(labels)
    weights = 1.0 / class_counts[labels]
    sample_weights = torch.from_numpy(weights.astype(np.float32))
    return WeightedRandomSampler(sample_weights, num_samples=len(sample_weights), replacement=True)


def build_dataset_from_npz(path: str) -> FeatureDataset:
    """Build a FeatureDataset from an NPZ data source."""
    features, labels, scores = load_npz_data(path)
    return FeatureDataset(features, labels, scores)


def build_dataset_from_image_folders(root_dir: str, partition: str = "train") -> FeatureDataset:
    """Load a dataset split from the precomputed feature archives.

    Training uses fixed-length feature vectors, so this helper maps the
    image-dataset root to the NPZ files generated by `extract_features.py`.
    """
    candidate_paths = [
        Path("features") / f"{partition}.npz",
        Path(root_dir) / f"{partition}.npz",
        Path(root_dir).parent / "features" / f"{partition}.npz",
    ]
    for candidate in candidate_paths:
        if candidate.exists():
            return build_dataset_from_npz(str(candidate))

    searched = ", ".join(str(path) for path in candidate_paths)
    raise FileNotFoundError(
        f"Could not find precomputed features for partition '{partition}'. "
        f"Looked in: {searched}. Run extract_features.py first or pass --data-path."
    )

